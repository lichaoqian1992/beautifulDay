package com.manji.backstage.model.content;

public class Channel {

	int id;
	int site_id;
	String name;
	String title;
	int is_albums;
	int is_attach;
	int is_spec;
	int sort_id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getSite_id() {
		return site_id;
	}
	public void setSite_id(int site_id) {
		this.site_id = site_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getIs_albums() {
		return is_albums;
	}
	public void setIs_albums(int is_albums) {
		this.is_albums = is_albums;
	}
	public int getIs_attach() {
		return is_attach;
	}
	public void setIs_attach(int is_attach) {
		this.is_attach = is_attach;
	}
	public int getIs_spec() {
		return is_spec;
	}
	public void setIs_spec(int is_spec) {
		this.is_spec = is_spec;
	}
	public int getSort_id() {
		return sort_id;
	}
	public void setSort_id(int sort_id) {
		this.sort_id = sort_id;
	}
	
	
	
	

	
}
