package com.manji.backstage.model.operation;

public class Advert {
	int id;
	String title;
	String call_index;
	double price;
	int show_numbers;
	int buy_numbers;
	String remark;
	String type;
	String price_remark;
	int view_width;
	int view_height;
	String target;
	int advert_type;
	String add_time;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getCall_index() {
		return call_index;
	}
	public void setCall_index(String call_index) {
		this.call_index = call_index;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public int getShow_numbers() {
		return show_numbers;
	}
	public void setShow_numbers(int show_numbers) {
		this.show_numbers = show_numbers;
	}
	public int getBuy_numbers() {
		return buy_numbers;
	}
	public void setBuy_numbers(int buy_numbers) {
		this.buy_numbers = buy_numbers;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getPrice_remark() {
		return price_remark;
	}
	public void setPrice_remark(String price_remark) {
		this.price_remark = price_remark;
	}
	public int getView_width() {
		return view_width;
	}
	public void setView_width(int view_width) {
		this.view_width = view_width;
	}
	public int getView_height() {
		return view_height;
	}
	public void setView_height(int view_height) {
		this.view_height = view_height;
	}
	public String getTarget() {
		return target;
	}
	public void setTarget(String target) {
		this.target = target;
	}
	public int getAdvert_type() {
		return advert_type;
	}
	public void setAdvert_type(int advert_type) {
		this.advert_type = advert_type;
	}
	public String getAdd_time() {
		return add_time;
	}
	public void setAdd_time(String add_time) {
		this.add_time = add_time;
	}
	
	
}
