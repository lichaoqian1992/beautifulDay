package com.manji.backstage.model.property;

public class SmsTemplate {

	int id;
	String title;
	String call_index;
	String content;
	int is_sys;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the call_index
	 */
	public String getCall_index() {
		return call_index;
	}
	/**
	 * @param call_index the call_index to set
	 */
	public void setCall_index(String call_index) {
		this.call_index = call_index;
	}
	/**
	 * @return the content
	 */
	public String getContent() {
		return content;
	}
	/**
	 * @param content the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}
	/**
	 * @return the is_sys
	 */
	public int getIs_sys() {
		return is_sys;
	}
	/**
	 * @param is_sys the is_sys to set
	 */
	public void setIs_sys(int is_sys) {
		this.is_sys = is_sys;
	}
	
	
}
