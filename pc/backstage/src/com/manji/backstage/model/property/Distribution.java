package com.manji.backstage.model.property;

public class Distribution {

	int id;
	String title;
	String express_code;
	double express_fee;
	String remark;
	int sort_id;
	int is_lock;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the express_code
	 */
	public String getExpress_code() {
		return express_code;
	}
	/**
	 * @param express_code the express_code to set
	 */
	public void setExpress_code(String express_code) {
		this.express_code = express_code;
	}
	/**
	 * @return the express_fee
	 */
	public double getExpress_fee() {
		return express_fee;
	}
	/**
	 * @param express_fee the express_fee to set
	 */
	public void setExpress_fee(double express_fee) {
		this.express_fee = express_fee;
	}
	/**
	 * @return the remark
	 */
	public String getRemark() {
		return remark;
	}
	/**
	 * @param remark the remark to set
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}
	/**
	 * @return the sort_id
	 */
	public int getSort_id() {
		return sort_id;
	}
	/**
	 * @param sort_id the sort_id to set
	 */
	public void setSort_id(int sort_id) {
		this.sort_id = sort_id;
	}
	/**
	 * @return the is_lock
	 */
	public int getIs_lock() {
		return is_lock;
	}
	/**
	 * @param is_lock the is_lock to set
	 */
	public void setIs_lock(int is_lock) {
		this.is_lock = is_lock;
	}
	
	
	
}
