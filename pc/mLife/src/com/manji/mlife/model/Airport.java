package com.manji.mlife.model;

public class Airport {
	private String airId;//机场三字码
	private String airName;//机场名称
	private String airJm;//机场简码
	private String airProvince;//机场所属城市
	public String getAirId() {
		return airId;
	}
	public void setAirId(String airId) {
		this.airId = airId;
	}
	public String getAirName() {
		return airName;
	}
	public void setAirName(String airName) {
		this.airName = airName;
	}
	public String getAirJm() {
		return airJm;
	}
	public void setAirJm(String airJm) {
		this.airJm = airJm;
	}
	public String getAirProvince() {
		return airProvince;
	}
	public void setAirProvince(String airProvince) {
		this.airProvince = airProvince;
	}
}
