package com.manji.mlife.model;

public class AirAppBean {

	
	private String airLineJson;
	private String passengers;
	private String contactJson;
	public String getAirLineJson() {
		return airLineJson;
	}
	public void setAirLineJson(String airLineJson) {
		this.airLineJson = airLineJson;
	}
	public String getPassengers() {
		return passengers;
	}
	public void setPassengers(String passengers) {
		this.passengers = passengers;
	}
	public String getContactJson() {
		return contactJson;
	}
	public void setContactJson(String contactJson) {
		this.contactJson = contactJson;
	}
	
	
	
}
