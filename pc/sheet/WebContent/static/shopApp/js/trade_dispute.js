$(function(){
	$(".header_title li .span1").click(function(){
		$(".header_title li span").removeClass("span_active");
		$(this).addClass("span_active");
		$(".content").show();
		$(".content2").hide();
		$(".footer").show();
	})
	$(".header_title li .span2").click(function(){
		$(".header_title li span").removeClass("span_active");
		$(this).addClass("span_active");
		$(".content").hide();
		$(".content2").show();
		$(".footer").hide();
	})
})
