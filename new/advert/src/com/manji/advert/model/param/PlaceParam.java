package com.manji.advert.model.param;

public class PlaceParam {

	private int index;
	private int id;
	private int channel;
	private String state;
	private String sort;
	private String title;
	private String max;
	private String wpercen;
	private String hpercen;
	private String remark;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public int getChannel() {
		return channel;
	}
	public void setChannel(int channel) {
		this.channel = channel;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getMax() {
		return max;
	}
	public void setMax(String max) {
		this.max = max;
	}
	public String getWpercen() {
		return wpercen;
	}
	public void setWpercen(String wpercen) {
		this.wpercen = wpercen;
	}
	public String getHpercen() {
		return hpercen;
	}
	public void setHpercen(String hpercen) {
		this.hpercen = hpercen;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
	
}
