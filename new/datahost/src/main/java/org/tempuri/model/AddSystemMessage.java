package org.tempuri.model;

//消息推送
public class AddSystemMessage {

	private Integer accept_user_id;//收信人user_id
	private String accept_user_role_type;//收信人角色
	private Integer accept_user_role_value;//收信人角色值
	private String businessNumber;//业务号
	private String content;//消息内容（站内信，推送消息）
	private boolean isSendJPush;//是否极光推送
	private String loginType;//极光推送设备（App_IOS，App_Android），为空时全部设备推送
	private boolean isSms;//是否发送短信
	private String mobile;//短信接收手机号（多个用逗号隔开）
	private String smsContent;//短信消息内容
	private String title;//消息标题（用于站内信）
	private Integer u_type;//0推送给用户，1推送给商家
	
	public boolean isSendJPush() {
		return isSendJPush;
	}
	public void setSendJPush(boolean isSendJPush) {
		this.isSendJPush = isSendJPush;
	}
	public boolean isSms() {
		return isSms;
	}
	public void setSms(boolean isSms) {
		this.isSms = isSms;
	}
	public Integer getAccept_user_id() {
		return accept_user_id;
	}
	public void setAccept_user_id(Integer accept_user_id) {
		this.accept_user_id = accept_user_id;
	}
	public String getAccept_user_role_type() {
		return accept_user_role_type;
	}
	public void setAccept_user_role_type(String accept_user_role_type) {
		this.accept_user_role_type = accept_user_role_type;
	}
	public Integer getAccept_user_role_value() {
		return accept_user_role_value;
	}
	public void setAccept_user_role_value(Integer accept_user_role_value) {
		this.accept_user_role_value = accept_user_role_value;
	}
	public String getBusinessNumber() {
		return businessNumber;
	}
	public void setBusinessNumber(String businessNumber) {
		this.businessNumber = businessNumber;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getLoginType() {
		return loginType;
	}
	public void setLoginType(String loginType) {
		this.loginType = loginType;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getSmsContent() {
		return smsContent;
	}
	public void setSmsContent(String smsContent) {
		this.smsContent = smsContent;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Integer getU_type() {
		return u_type;
	}
	public void setU_type(Integer u_type) {
		this.u_type = u_type;
	}
	
}
